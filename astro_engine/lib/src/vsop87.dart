/// Truncated VSOP87 planetary theory (Meeus "Astronomical Algorithms").
///
/// Computes heliocentric ecliptic coordinates for Earth and planets,
/// then converts to geocentric ecliptic coordinates.
///
/// Coordinates are referred to the mean dynamical ecliptic and equinox of date.

import 'dart:math';
import 'julian.dart';
import 'zodiac.dart';

// ─── VSOP87 Term Evaluator ───────────────────────────────────────────────

/// Evaluate one coefficient series: sum(A * cos(B + C * tau)).
/// [terms] is a flat list with stride 3: [A0, B0, C0, A1, B1, C1, ...].
double _evalTerms(List<double> terms, double tau) {
  double sum = 0;
  for (int i = 0; i < terms.length; i += 3) {
    sum += terms[i] * cos(terms[i + 1] + terms[i + 2] * tau);
  }
  return sum;
}

/// Evaluate a full VSOP87 series: L0 + L1*tau + L2*tau^2 + ...
/// Meeus tables give amplitudes in units of 10^-8, so we divide by 1e8.
double _evalSeries(List<List<double>> series, double tau) {
  double result = 0;
  double tauPow = 1;
  for (final terms in series) {
    result += _evalTerms(terms, tau) * tauPow;
    tauPow *= tau;
  }
  return result / 1e8;
}

// ─── Planet Position Result ──────────────────────────────────────────────

class PlanetPosition {
  /// Geocentric ecliptic longitude (degrees, apparent)
  final double lon;

  /// Geocentric ecliptic latitude (degrees)
  final double lat;

  /// Distance from Earth (AU)
  final double dist;

  /// Longitude speed (degrees/day)
  final double lonSpeed;

  /// Latitude speed (degrees/day)
  final double latSpeed;

  const PlanetPosition({
    required this.lon,
    required this.lat,
    required this.dist,
    required this.lonSpeed,
    required this.latSpeed,
  });
}

// ─── Public API ──────────────────────────────────────────────────────────

/// Compute the Sun's geocentric apparent position.
PlanetPosition sunPosition(double jd, double deltaPsi) {
  final tau = julianMillennia(jd);

  // Earth heliocentric position
  double eL = _evalSeries(_earthL, tau);
  double eB = _evalSeries(_earthB, tau);
  double eR = _evalSeries(_earthR, tau);

  // Sun = Earth + 180°
  double sunLon = eL * 180 / pi + 180;
  double sunLat = -eB * 180 / pi;
  double sunR = eR;

  // Aberration correction
  sunLon -= 0.00569;

  // Nutation
  sunLon += deltaPsi;

  sunLon = normalizeDeg(sunLon);

  // Speed via finite difference (±0.5 day)
  final speed = _sunSpeed(jd);

  return PlanetPosition(
    lon: sunLon,
    lat: sunLat,
    dist: sunR,
    lonSpeed: speed.$1,
    latSpeed: speed.$2,
  );
}

(double lonSpeed, double latSpeed) _sunSpeed(double jd) {
  const dt = 0.5;
  final tau1 = julianMillennia(jd - dt);
  final tau2 = julianMillennia(jd + dt);

  double l1 = _evalSeries(_earthL, tau1) * 180 / pi + 180;
  double l2 = _evalSeries(_earthL, tau2) * 180 / pi + 180;
  double b1 = -_evalSeries(_earthB, tau1) * 180 / pi;
  double b2 = -_evalSeries(_earthB, tau2) * 180 / pi;

  double dlon = l2 - l1;
  if (dlon > 180) dlon -= 360;
  if (dlon < -180) dlon += 360;

  return (dlon / (2 * dt), (b2 - b1) / (2 * dt));
}

/// Compute a planet's geocentric apparent position.
/// [planet] is one of: 'Mercury', 'Venus', 'Mars', 'Jupiter', 'Saturn'.
PlanetPosition planetPosition(String planet, double jd, double deltaPsi) {
  final tau = julianMillennia(jd);

  // Earth heliocentric
  double eL = _evalSeries(_earthL, tau);
  double eB = _evalSeries(_earthB, tau);
  double eR = _evalSeries(_earthR, tau);

  // Planet heliocentric
  final data = _planetData[planet]!;
  double pL = _evalSeries(data.$1, tau); // radians
  double pB = _evalSeries(data.$2, tau);
  double pR = _evalSeries(data.$3, tau);

  // Convert to geocentric ecliptic
  final geo = _helioToGeo(pL, pB, pR, eL, eB, eR);
  double lon = normalizeDeg(
    geo.$1 + deltaPsi - 0.00569,
  ); // nutation + aberration
  double lat = geo.$2;

  // Speed via finite difference
  final speed = _planetSpeed(planet, jd, deltaPsi);

  return PlanetPosition(
    lon: lon,
    lat: lat,
    dist: geo.$3,
    lonSpeed: speed.$1,
    latSpeed: speed.$2,
  );
}

(double lonSpeed, double latSpeed) _planetSpeed(
  String planet,
  double jd,
  double deltaPsi,
) {
  const dt = 0.5;
  final p1 = _rawPlanetGeoLonLat(planet, jd - dt);
  final p2 = _rawPlanetGeoLonLat(planet, jd + dt);

  double dlon = p2.$1 - p1.$1;
  if (dlon > 180) dlon -= 360;
  if (dlon < -180) dlon += 360;

  return (dlon / (2 * dt), (p2.$2 - p1.$2) / (2 * dt));
}

(double lon, double lat) _rawPlanetGeoLonLat(String planet, double jd) {
  final tau = julianMillennia(jd);
  double eL = _evalSeries(_earthL, tau);
  double eB = _evalSeries(_earthB, tau);
  double eR = _evalSeries(_earthR, tau);
  final data = _planetData[planet]!;
  double pL = _evalSeries(data.$1, tau);
  double pB = _evalSeries(data.$2, tau);
  double pR = _evalSeries(data.$3, tau);
  final geo = _helioToGeo(pL, pB, pR, eL, eB, eR);
  return (geo.$1, geo.$2);
}

/// Convert heliocentric ecliptic to geocentric ecliptic.
/// All angles in radians. Returns (lon_deg, lat_deg, dist_AU).
(double, double, double) _helioToGeo(
  double pL,
  double pB,
  double pR,
  double eL,
  double eB,
  double eR,
) {
  double x = pR * cos(pB) * cos(pL) - eR * cos(eB) * cos(eL);
  double y = pR * cos(pB) * sin(pL) - eR * cos(eB) * sin(eL);
  double z = pR * sin(pB) - eR * sin(eB);

  double lon = atan2(y, x) * 180 / pi;
  if (lon < 0) lon += 360;
  double lat = atan2(z, sqrt(x * x + y * y)) * 180 / pi;
  double dist = sqrt(x * x + y * y + z * z);

  return (lon, lat, dist);
}

